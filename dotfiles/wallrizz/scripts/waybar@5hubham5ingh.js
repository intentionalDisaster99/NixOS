export function getDarkThemeConf(colors) {
    return generateWaybarTheme(colors);
}

export function getLightThemeConf(colors) {
    return generateWaybarTheme(colors);
}

function generateWaybarTheme(colors) {
    const bg = (colors && colors[0]) ? colors[0] : "#24273a";
    const fg = (colors && colors[1]) ? colors[1] : "#cad3f5";
    const acc1 = (colors && colors[2]) ? colors[2] : "#8aadf4";
    const acc2 = (colors && colors[3]) ? colors[3] : "#a6da95";
    const acc3 = (colors && colors[4]) ? colors[4] : "#f5a97f";

    const formatCol = (c) => c.startsWith('#') ? c : '#' + c;

    return `/* Waybar Colors dynamically generated by WallRizz */
@define-color base   ${formatCol(bg)};
@define-color mantle ${formatCol(bg)};
@define-color crust  ${formatCol(bg)};

@define-color text     ${formatCol(fg)};
@define-color subtext0 ${formatCol(fg)};
@define-color subtext1 ${formatCol(fg)};

@define-color surface0 ${formatCol(bg)};
@define-color surface1 ${formatCol(bg)};
@define-color surface2 ${formatCol(bg)};

@define-color overlay0 ${formatCol(bg)};
@define-color overlay1 ${formatCol(fg)};
@define-color overlay2 ${formatCol(fg)};

@define-color blue      ${formatCol(acc1)};
@define-color lavender  ${formatCol(acc1)};
@define-color sapphire  ${formatCol(acc1)};
@define-color sky       ${formatCol(acc2)};
@define-color teal      ${formatCol(acc2)};
@define-color green     ${formatCol(acc2)};
@define-color yellow    ${formatCol(acc3)};
@define-color peach     ${formatCol(acc3)};
@define-color maroon    ${formatCol(acc1)};
@define-color red       ${formatCol(acc1)};
@define-color mauve     ${formatCol(acc3)};
@define-color pink      ${formatCol(acc3)};
@define-color flamingo  ${formatCol(acc1)};
@define-color rosewater ${formatCol(acc1)};
`;
}

export function setTheme(newThemeConfigPath) {
    const cacheDir = HOME_DIR.concat("/.cache/wallrizz");
    const liveConfigPath = cacheDir.concat("/waybar.css");
    OS.exec(["mkdir", "-p", cacheDir]);

    const newThemeConfig = STD.loadFile(newThemeConfigPath);

    if (newThemeConfig && !newThemeConfig.includes("cursor_text_color")) {
        const file = STD.open(liveConfigPath, "w");
        if (file) {
            file.puts(newThemeConfig);
            file.close();
        }
    }

    OS.exec(["sh", "-c", "pkill -SIGUSR2 waybar > /dev/null 2>&1"]);
    return;
}