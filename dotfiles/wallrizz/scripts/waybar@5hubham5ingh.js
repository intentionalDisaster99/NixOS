export function generateTheme(colors, isDark = true) {
    const getCol = (c, fallback) => {
        if (!c || c === "undefined" || c === "null" || String(c).trim() === "") {
            return fallback;
        }
        let cleanColor = String(c).trim();
        return cleanColor.startsWith('#') ? cleanColor : '#' + cleanColor;
    };

    return `/* Waybar Colors dynamically generated by WallRizz */
/* Base & Backgrounds */
@define-color base   ${getCol(colors.background, "#24273a")};
@define-color mantle ${getCol(colors.color0, "#1e2030")};
@define-color crust  ${getCol(colors.color0, "#181926")};

/* Text */
@define-color text     ${getCol(colors.foreground, "#cad3f5")};
@define-color subtext0 ${getCol(colors.color7, "#a5adcb")};
@define-color subtext1 ${getCol(colors.color15, "#b8c0e0")};

/* Surfaces & Overlays */
@define-color surface0 ${getCol(colors.color8, "#363a4f")};
@define-color surface1 ${getCol(colors.color8, "#494d64")};
@define-color surface2 ${getCol(colors.color8, "#5b6078")};
@define-color overlay0 ${getCol(colors.color8, "#6e738d")};
@define-color overlay1 ${getCol(colors.color7, "#8087a2")};
@define-color overlay2 ${getCol(colors.color7, "#939ab7")};

/* Accents mapped to Wallrizz dynamic palette */
@define-color blue      ${getCol(colors.color4, "#8aadf4")};
@define-color lavender  ${getCol(colors.color4, "#b7bdf8")};
@define-color sapphire  ${getCol(colors.color4, "#7dc4e4")};
@define-color sky       ${getCol(colors.color6, "#91d7e3")};
@define-color teal      ${getCol(colors.color6, "#8bd5ca")};
@define-color green     ${getCol(colors.color2, "#a6da95")};
@define-color yellow    ${getCol(colors.color3, "#eed49f")};
@define-color peach     ${getCol(colors.color3, "#f5a97f")};
@define-color maroon    ${getCol(colors.color1, "#ee99a0")};
@define-color red       ${getCol(colors.color1, "#ed8796")};
@define-color mauve     ${getCol(colors.color5, "#c6a0f6")};
@define-color pink      ${getCol(colors.color5, "#f5bde6")};
@define-color flamingo  ${getCol(colors.color1, "#f0c6c6")};
@define-color rosewater ${getCol(colors.color1, "#f4dbd6")};
`;
}

export function setTheme(newThemeConfigPath) {
    const cacheDir = HOME_DIR.concat("/.cache/wallrizz");
    const liveConfigPath = cacheDir.concat("/waybar.css");

    OS.exec(["mkdir", "-p", cacheDir]);

    const newThemeConfig = STD.loadFile(newThemeConfigPath);
    if (newThemeConfig) {
        const file = STD.open(liveConfigPath, "w");
        if (file) {
            file.puts(newThemeConfig);
            file.close();
            OS.exec(["pkill", "-SIGUSR2", "waybar"]);
        }
    }
}